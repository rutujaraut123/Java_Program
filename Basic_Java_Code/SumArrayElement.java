import java.io.*;
import java.util.*;
public class SumArrayElement
{
                  public static void main(String arg[])
                  {
                                    int array[]={10,20,30,40,50};
                                    int Sum=0;
                                    
                                    for(int i=0;i<array.length;i++)
                                    {
                                                      Sum=Sum+array[i];
                                                      
                                    }
                                    System.out.println("Sum="+Sum);
                                    
                                
                  }
}